package boot.camp.springjsf.config;

public class ConfigServiceException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -7795727845566018999L;

	public ConfigServiceException(String message) {
		super(message);
	}

	public ConfigServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public ConfigServiceException(Throwable cause) {
		super(cause);
	}


}
